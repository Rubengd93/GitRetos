
function calculator(operator,num1, num2) {

    if (operator === "sum") {
          
          return num1 + num2;

    }
    if (operator === "subs") {
          
           return num1 - num2;

     }
     if (operator === "mult") {
          
           return num1 * num2;

     }
     if (operator === "div") {
          
           return num1 / num2;

     }

}

